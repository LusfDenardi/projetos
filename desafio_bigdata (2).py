import os, numpy as np, joblib
import polars as pl

def slope_calib(y, yhat, lo=0.90, hi=1.30):
    # a* = argmin ||y - a*yhat||¬≤  ‚Üí solu√ß√£o fechada
    num = float((yhat * y).sum())
    den = float((yhat * yhat).sum()) + 1e-12
    a = num / den
    return float(np.clip(a, lo, hi))

